nums = [64, 34, 25, 12, 22, 11, 90]
bubble_sort(nums.copy())
print("Bubble Sort:", nums)

selection_sort(nums.copy())
print("Selection Sort:", nums)

insertion_sort(nums.copy())
print("Insertion Sort:", nums)

shell_sort(nums.copy())
print("Shell Sort:", nums)

merge_sort(nums.copy())
print("Merge Sort:", nums)

quick_sort(nums.copy(), 0, len(nums) - 1)
print("Quick Sort:", nums)

heap_sort(nums.copy())
